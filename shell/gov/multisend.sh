for amt in "${AMOUNTS[@]}"; do
    grand_total=$(echo "$grand_total + $amt" | bc)
done

if [ "$grand_total" != "$TOTAL_AMOUNT" ]; then
    echo "Error: Sum of all amounts ($grand_total) does not match TOTAL_AMOUNT ($TOTAL_AMOUNT)."
    exit 1
