processed_total=0

for ((i=0; i<length; i+=batch_size)); do
    batch_users=("${USERS[@]:i:batch_size}")
    batch_amounts=("${AMOUNTS[@]:i:batch_size}")

    # Compute subtotal for the batch
    subtotal=0
    for amt in "${batch_amounts[@]}"; do
        subtotal=$(echo "$subtotal + $amt" | bc)
    done

    processed_total=$(echo "$processed_total + $subtotal" | bc)

    # Format arrays as comma-separated strings
    IFS=, batch_users_str="${batch_users[*]}"
    IFS=, batch_amounts_str="${batch_amounts[*]}"

    echo "Processing batch starting at index $i with ${#batch_users[@]} users and subtotal $subtotal"

    forge script script/gov/InfraredMultisigGovernance.s.sol:InfraredMultisigGovernance \
